            # aligned_pred_quad,mask_quad,_ = self.align_net(gt_qua,self.output[0],scale=1.0)
            # aligned_pred_half,mask_half,_=self.align_net(gt_hal,self.output[1],scale=1.0)
            # aligned_pred,mask,_=self.align_net(self.gt,self.output[2],scale=1.0)
            # #
            # #
